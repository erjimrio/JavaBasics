public class GameStart {
    public static void main(String[] args){
        int score = 0; //initialize integer
        double time = 0.0; // initialize decimal
        char rank = 'C'; // initialize character
        boolean isGameOver = false; // initialize boolean
        String playerName = "Player1"; //initialize string
        System.out.println("Player: " + playerName);
        System.out.println("Starting Score: " + score);
        System.out.println("Time: " + time + " seconds");
        System.out.println("Rank: " + rank);
        System.out.println("Game Over: " + isGameOver);
    }
}
