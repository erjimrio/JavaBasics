public class NameYourVariables {
    public static void main(String[] args){
        int playerScore = 100; // descriptive name
        double totalAmount = 250.75; //camelCase format
        boolean isWinner = true; // starts with lowercase
        String userName = "Alex"; //no spaces or numbers at start
        char grade = 'A'; // single letter name is okay too
        System.out.println("Player Score: " + playerScore);
        System.out.println("Total Amount: " + totalAmount);
        System.out.println("Is Winner: " + isWinner);
        System.out.println("User Name: " + userName);
        System.out.println("Grade: " + grade);
    }
}
