public class GradeDisplay {
    public static void main(String[] args){
        char grade = 'A'; // student's grade
        System.out.println("Student Grade: " + grade);
        // change the grade
        grade = 'B';
        System.out.println("Update Grade: " + grade);
        // use a symbol
        char symbol = '✔';
        System.out.println("Passed: " + symbol);
    }
}
