public class MiniProfile {
    public static void main(String[] args){
        byte age = 25; // small whole number
        short height = 170; // medium whole number
        int id = 123456; // standard number
        long phoneNumber = 9876543210L; // large number
        float weight = 65.5f; // decimal number
        double balance = 2500.75; // more precise decimal
        char grade = 'A'; // single letter
        boolean isStudent = true; // true or false
        System.out.println("Age: " + age);
        System.out.println("Height: " + height);
        System.out.println("ID: " + id);
        System.out.println("Phone: " + phoneNumber);
        System.out.println("Weight: " + weight);
        System.out.println("Balance: " + balance);
        System.out.println("Grade: " + grade);
        System.out.println("Student: " + isStudent);
    }
}
